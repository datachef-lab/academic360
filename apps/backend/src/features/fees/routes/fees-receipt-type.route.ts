import { Router } from "express";
import { getFeesReceiptTypesHandler, getFeesReceiptTypeByIdHandler, createFeesReceiptTypeHandler, updateFeesReceiptTypeHandler, deleteFeesReceiptTypeHandler } from "../controllers/fees-receipt-type.controller.js";

const feesReceiptTypeRouter = Router();

feesReceiptTypeRouter.get("/", getFeesReceiptTypesHandler);
feesReceiptTypeRouter.get("/:id", getFeesReceiptTypeByIdHandler);
feesReceiptTypeRouter.post("/", createFeesReceiptTypeHandler);
feesReceiptTypeRouter.put("/:id", updateFeesReceiptTypeHandler);
feesReceiptTypeRouter.delete("/:id", deleteFeesReceiptTypeHandler);

export default feesReceiptTypeRouter;
