import { Router } from "express";
import { getFeesComponentsHandler, getFeesComponentByIdHandler, createFeesComponentHandler, updateFeesComponentHandler, deleteFeesComponentHandler } from "../controllers/feesComponent.controller.js";

const feesComponentRouter = Router();

feesComponentRouter.get("/", getFeesComponentsHandler);
feesComponentRouter.get("/:id", getFeesComponentByIdHandler);
feesComponentRouter.post("/", createFeesComponentHandler);
feesComponentRouter.put("/:id", updateFeesComponentHandler);
feesComponentRouter.delete("/:id", deleteFeesComponentHandler);

export default feesComponentRouter;
